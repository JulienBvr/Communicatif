    public void recepMessage(String s, String m)
    {
        // Synchro liste graphique
        dlm.addElement(s + " : " + m);
    }
    
    public void initMessages(ArrayList<String> list)
    {
        for(String msg : list)
        {
            dlm.addElement(msg);
        }
    }
